// build-icon-sprite.mjs
// Scans the frontend for Tabler icon names actually in use and emits a single
// inlined SVG sprite (only the used icons) plus a manifest Set. Universal
// browser support: the sprite is injected once into the document and every
// icon is referenced via same-document <use href="#tabler-NAME">, which works
// in every browser including Safari (no cross-file <use>).
//
// Run after adding/removing icons:  node scripts/build-icon-sprite.mjs
//
// Reads:  src/frontend/assets/icons/{outline,filled}/*.svg  (vendored Tabler)
//         all .jsx under src/frontend and src/plugins        (usage scan)
// Writes: src/frontend/assets/iconSprite.js                  (SPRITE + ICON_NAMES)
import { readdirSync, readFileSync, writeFileSync, statSync, existsSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const ICONS = join(root, "src/frontend/assets/icons");
const SCAN_DIRS = [join(root, "src/frontend"), join(root, "src/plugins")];

// Icon names that are built dynamically in code (string concatenation) and so
// cannot be found by a literal scan. Keep this list in sync with such usages.
const SEED = ["chevron-down", "chevron-right"];

function walk(dir, out = []) {
  if (!existsSync(dir)) return out;
  for (const name of readdirSync(dir)) {
    const p = join(dir, name);
    const st = statSync(p);
    if (st.isDirectory()) walk(p, out);
    else if (/\.(jsx?|tsx?)$/.test(name)) out.push(p);
  }
  return out;
}

// Collect candidate tokens (without the ti- prefix) from all source files.
const tokens = new Set(SEED);
const RE = /\bti-([a-z0-9]+(?:-[a-z0-9]+)*)\b/g;
for (const dir of SCAN_DIRS) {
  for (const file of walk(dir)) {
    const src = readFileSync(file, "utf8");
    let m;
    while ((m = RE.exec(src))) tokens.add(m[1]);
  }
}

// Resolve each token to a vendored SVG file. A "-filled" suffix maps to the
// filled set with the suffix stripped from the filename.
const BBOX = /<path\b[^>]*\bd="M0 0h24v24H0z"[^>]*\/>\s*/g;
function inner(svg) {
  return svg.replace(/^[\s\S]*?<svg\b[^>]*>/, "").replace(/<\/svg>\s*$/, "").replace(BBOX, "").trim();
}
function fileFor(token) {
  if (token.endsWith("-filled")) {
    const base = token.slice(0, -"-filled".length);
    const f = join(ICONS, "filled", base + ".svg");
    return existsSync(f) ? f : null;
  }
  const o = join(ICONS, "outline", token + ".svg");
  if (existsSync(o)) return o;
  const f = join(ICONS, "filled", token + ".svg");
  return existsSync(f) ? f : null;
}

const resolved = [];
const unresolved = [];
for (const token of [...tokens].sort()) {
  const f = fileFor(token);
  if (f) resolved.push([token, f]);
  else unresolved.push(token);
}

const symbols = resolved
  .map(([token, f]) => `<symbol id="tabler-${token}" viewBox="0 0 24 24">${inner(readFileSync(f, "utf8"))}</symbol>`)
  .join("");
const sprite = `<svg xmlns="http://www.w3.org/2000/svg" aria-hidden="true" style="position:absolute;width:0;height:0;overflow:hidden">${symbols}</svg>`;

const out =
  `// AUTO-GENERATED by scripts/build-icon-sprite.mjs - do not edit by hand.\n` +
  `// Inlined Tabler icon sprite (${resolved.length} icons) + name manifest.\n` +
  `export const SPRITE = ${JSON.stringify(sprite)};\n` +
  `export const ICON_NAMES = new Set(${JSON.stringify(resolved.map(([t]) => t))});\n`;
writeFileSync(join(root, "src/frontend/assets/iconSprite.js"), out);

console.log(`resolved ${resolved.length} icons, sprite ${(Buffer.byteLength(sprite) / 1024).toFixed(0)} KB`);
console.log(`unresolved (${unresolved.length}):`, unresolved.join(" ") || "(none)");
